const checkData = (tokenAddress:string, batchTransferAddress: string, fromAddress: string): boolean => {
  const { addrs, amounts } = data;

  if (!addrs || addrs.length !== 21)
    return false;
  if (!amounts || amounts.length !== 21)
    return false;
  for (let i = 0; i < addrs.length; i++) {
    if (addrs[i].length !== amounts[i].length) {
      return false;
    }
  }

  if (data.tokenAddress !== tokenAddress)
    return false;
  if (data.batchTransferAddress !== batchTransferAddress)
    return false;
  if (data.fromAddress !== fromAddress)
    return false;

  return true;
};

export const initialize = (tokenAddress:string, batchTransferAddress: string, fromAddress: string) : boolean => {
  setData(tokenAddress, batchTransferAddress, fromAddress);
  return checkData(tokenAddress, batchTransferAddress, fromAddress);
};

export const airdropTransfer = async (index: number): Promise<any> => {
  const { addrs, amounts, fromAddress, batchTransferAddress, sentList } = data;

  if (index < 0 || index >= addrs.length) {
    console.log('Check the index parameter.');
    return false;
  } else if (sentList.has(index)) {
    console.log('Can only be run once.');
    return false;
  }

  const metadata = JSON.parse(await remix.call('fileManager', 'getFile', 'contracts/artifacts/BatchTransfer.json'));
  const contract = new web3.eth.Contract(metadata.abi, batchTransferAddress);
  const accounts = await web3.eth.getAccounts();

  const ret = await contract.methods.batchTransferFrom(fromAddress, addrs[index], amounts[index]).send({from:accounts[0]});
  if (ret) {
    sentList.add(index);
  }

  return ret;
};

export const checkTransferResult = async (index: number): Promise<any> => {
  const { addrs, amounts, tokenAddress } = data;

  const metadata = JSON.parse(await remix.call('fileManager', 'getFile', 'contracts/artifacts/ORT.json'));
  const contract = new web3.eth.Contract(metadata.abi, tokenAddress);

  const ret = [];
  for (const i in addrs[index]) {
    const savedData = await contract.methods.balanceOf(addrs[index][i]).call();
    if (savedData !== amounts[index][i]) {
      ret.push({addr: addrs[index][i], saved: savedData, planned: amounts[index][i]});
    }
  }

  return ret;
};

